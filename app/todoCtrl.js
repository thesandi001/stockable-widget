    $scope.addToBasket = function() {        
        var query = '';        
        var list = [];
        if($scope.tabValue == 'current') {
            list = $scope.currentList;            
        } else if($scope.tabValue == 'history') {
            list = $scope.todoList;            
        }
        for(var i=0; i<list.length; i++) {
            if(list[i].completed) {
                if(query != '') query += '&';
                query += 'stocks='+list[i].sid;
            }
        }
        window.open(createBasketUrl+query, '_blank');        
    }

